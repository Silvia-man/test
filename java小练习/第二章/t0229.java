public class t0229 {
    public static void main(String args[]){
        int numA = 10;
        int numB = 20;
        System.out.println("加法计算：" + (numA + numB));
        System.out.println("减法计算：" + (numA - numB));
        System.out.println("乘法计算：" + (numA * numB));
        System.out.println("除法计算：" + (numA / (double) numB));
    }
}
