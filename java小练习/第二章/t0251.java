public class t0251 {
    public static void main(String args[]) {

        int ch = 1;
        switch(ch){
            case 2: {
                System.out.println("内容是2");
            }
            case 1: {
                System.out.println("内容是1");
            }
            case 3: {
                System.out.println("内容是3");
            }
            default: {
                System.out.println("没有匹配内容");
            }
        }
    }

}